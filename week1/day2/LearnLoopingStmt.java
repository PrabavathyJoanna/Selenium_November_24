package week1.day2;

public class LearnLoopingStmt {

	public static void main(String[] args) {

		int number=5;
		
		//for-> ctrl+ space => choose 2nd option
		for (int i = 0; i < number; i++) {
			System.out.println(i);
		}
		
		System.out.println();
		
		for (int i = 10; i >=0; i--) {
			System.out.println(i);
		}
	}

}
